package com.aiproject.smartcampus.model.prompts;

import org.springframework.stereotype.Component;

/**
 * @program: SmartCampus
 * @description: 用户提示词
 * @author: lk
 * @create: 2025-06-04 17:22
 **/
@Component
public class UserPrompts {

    public static final String RAG_PROMPT = "" +
            "你是一个任务链式执行助手，专门负责将前置任务的输出结果与当前任务需求相结合，生成新的可执行任务。\n" +
            "\n" +
            "## 执行流程\n" +
            "\n" +
            "### 1. 前置任务结果分析\n" +
            "**前置任务输出：**\n" +
            "{previous_task_output}\n" +
            "\n" +
            "**结果要点提取：**\n" +
            "- 关键信息：从前置任务中提取核心信息和关键数据\n" +
            "- 可用资源：识别可供后续任务使用的数据、结论、素材等\n" +
            "- 完成状态：确认前置任务的完成程度和输出质量\n" +
            "- **上下文关联**：请注意前置任务输出与当前任务的上下文关系，确保信息的连续性和一致性。\n" +
            "\n" +
            "### 2. 当前任务需求定义\n" +
            "**任务目标：**\n" +
            "{current_task_goal}\n" +
            "\n" +
            "**具体要求：**\n" +
            "- 输入需求：{input_requirements}\n" +
            "- 输出标准：{output_standards}\n" +
            "- 约束条件：{constraints}\n" +
            "\n" +
            "### 3. 依赖关系映射与任务融合\n" +
            "请分析前置任务输出与当前任务需求的关联关系：\n" +
            "1. **直接依赖**：前置任务的哪些输出直接服务于当前任务\n" +
            "2. **间接依赖**：前置任务的哪些信息可以为当前任务提供参考\n" +
            "3. **缺失补充**：识别前置任务输出与当前任务需求之间的缺口\n" +
            "\n" +
            "### 4. 融合任务执行\n" +
            "基于前置任务的输出结果，现在需要执行以下整合任务：\n" +
            "\n" +
            "**执行步骤：**\n" +
            "1. **继承处理**：充分利用前置任务的成果和数据，确保在当前任务中正确引用和使用。\n" +
            "2. **需求对接**：将前置结果与当前任务需求进行精准对接\n" +
            "3. **核心执行**：执行当前任务的核心操作和处理逻辑\n" +
            "4. **结果优化**：整合优化最终输出，确保质量和连贯性\n" +
            "\n" +
            "### 5. 输出要求\n" +
            "请提供以下格式的输出：\n" +
            "\n" +
            "**任务执行结果：**\n" +
            "[在此处提供整合后的任务执行结果]\n" +
            "\n" +
            "**关键信息传递：**\n" +
            "- 核心输出：[总结本任务的关键成果]\n" +
            "- 传递数据：[明确需要传递给下一任务的具体信息]\n" +
            "- 使用建议：[为下一任务使用本任务结果提供指导]\n" +
            "\n" +
            "### 质量检查标准\n" +
            "确保输出满足以下要求：\n" +
            "- ✓ 前置任务结果得到充分利用\n" +
            "- ✓ 当前任务目标完全达成\n" +
            "- ✓ 输出结果符合后续任务需要\n" +
            "- ✓ 整体逻辑连贯一致\n" +
            "\n" +
            "请严格按照上述流程执行，确保任务链的连续性和高质量输出。";

    public static String chainUserPrompts(String intent, String result) {
        return RAG_PROMPT.replace("{current_task_goal}", intent).replace("{previous_task_output}", result);
    }

    public static String getTaskPrompt(String intent, String result) {
        return String.format(
                "请注意：以下的意图（intent）是基于已有结果（result）生成的。  \n" +
                        "—— 已有结果（result）：%s  \n" +
                        "—— 对应意图（intent）：%s  \n\n" +
                        "请根据上述意图撰写一段完整的输出内容，要求覆盖以下几点：  \n" +
                        "1. 输出要围绕意图展开，确保与意图描述高度契合；  \n" +
                        "2. 逻辑清晰、条理分明，必要时可以分步骤说明；  \n" +
                        "3. 简洁但信息充分，方便后续模型或业务系统直接使用。  ",
                result,
                intent
        );
    }

    public static String getHandlerPrompts(String intent) {
        return String.format(
                """
                你是一个智能任务分析器，需要根据用户意图选择最合适的处理器。请按照以下优先级分析用户意图：
                
                用户意图：%s
                
                **分析流程（按优先级顺序）：**
                
                **第一优先级：自身能力评估**
                首先判断该意图是否可以通过LLM自身的知识和推理能力直接解决：
                - 一般性对话、问答
                - 创意写作、建议、分析
                - 基础知识解答
                - 逻辑推理、总结归纳
                - 情感支持、聊天互动
                - 语言翻译、文本处理
                
                **如果自身能力足够解决，默认选择：SteptIntentMemoryChatHandler**
                （在对话场景中，记忆功能几乎总是有益的，能提供更好的上下文连续性）
                
                **特殊情况下选择基础LLM处理器的场景：**
                - 系统初始化或测试场景
                - 明确标注为"忽略历史"的独立查询
                - 性能敏感且确定不需要上下文的场景
                
                **第二优先级：专门功能需求**
                如果自身能力不足，再根据具体需求选择专门处理器：
                
                1. **StepIntentFuncHanlder** - 工具函数处理器  
                   - 需要调用特定工具或执行具体功能操作
                   - 关键词：计算、执行、调用、操作、处理数据、生成图表、发送邮件、创建文件
                   - 示例：复杂数学计算、数据处理、文件操作、系统调用、API调用
                
                2. **SeptIntentRagHandler** - 增强检索生成处理器
                   - 需要基于特定知识库或文档检索信息
                   - 关键词：查询特定资料、搜索文档、检索知识库、获取专业资料
                   - 示例：查找特定文档、检索专业知识、基于企业知识库回答
                
                判断原则：
                1. **默认使用记忆对话处理器**：对于所有常规对话都应该保持记忆，提供连贯体验
                2. **明确的工具需求**：需要执行具体操作、调用外部工具时选择工具处理器
                3. **特定资料检索**：需要从知识库或文档中查找特定信息时选择RAG处理器
                4. **基础处理器**：仅在特殊场景下明确不需要记忆时使用
                
                请只返回处理器的类名，不要添加任何解释：
                - SeptIntentRagHandler
                - StepIntentFuncHanlder  
                - SteptIntentMemoryChatHandler
                - StepIntentLLMChatResponseHandler
                """,
                intent
        );
    }
}