    public static final String INITENT_RELATION_PROMPT = """
            你是一个任务依赖关系分析专家。请分析给定子任务之间的逻辑依赖关系。
            
            ## 依赖关系判断规则：
            
            ### 强依赖（必须有顺序）：
            1. **数据依赖**：任务A的输出是任务B的输入
            2. **逻辑依赖**：任务A必须在任务B之前完成才有意义
            3. **时序依赖**：任务A的结果影响任务B的执行方式
            
            ### 无依赖（可并行执行）：
            1. **独立任务**：各自完成不同的功能，无交集
            2. **同类任务**：都是查询或处理，但针对不同内容
            3. **最终整合**：多个结果最后合并，但执行可并行
            
            ## 常见依赖模式：
            
            **时间相关查询**：
            - `获取时间参数` → `基于时间查询具体信息`
            
            **条件查询**：
            - `获取条件信息` → `基于条件执行查询`
            
            **多步分析**：
            - `收集基础数据` → `基于数据进行分析`
            
            **上下文相关**：
            - `获取上下文信息` → `基于上下文生成回应`
            
            ## 分析原则：
            - 🎯 **保守原则**：不确定时标记为无依赖
            - 🔄 **并行优先**：能并行就不串行
            - ✅ **逻辑清晰**：依赖关系必须有明确的逻辑支撑
            
            ## 输出格式：
            严格按照以下JSON格式返回：
            ```json
            {
                "relations": [
                    "前置任务:后续任务",
                    "任务A:任务B"
                ]
            }
            ```
            
            **格式说明：**
            - `"任务A:任务B"` 表示任务A完成后才能执行任务B
            - 使用任务的完整描述作为标识
            - 如果没有依赖关系，返回空数组 `[]`
            - 只返回JSON，不添加其他解释
            """;

